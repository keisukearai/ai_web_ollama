    creds = Credentials.from_service_account_file(CREDENTIALS_PATH, scopes=scopes)
    gc = gspread.authorize(creds)
    sh = gc.open_by_key(SPREADSHEET_ID)
    ws = sh.sheet1

    # 34行目以降を削除（誤って追加した100行分）
    all_values = ws.get_all_values()
    total_rows = len(all_values)
    if total_rows > 33:
        ws.delete_rows(34, total_rows)
        print(f'{total_rows - 33}行の誤データを削除しました')

    # 正しいダミーデータを追加
    ws.append_rows(DUMMY_ROWS, value_input_option='USER_ENTERED')
    print(f'{len(DUMMY_ROWS)}行の架空会社Q&Aデータを追加しました')

